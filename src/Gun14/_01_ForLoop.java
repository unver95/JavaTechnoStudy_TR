package Gun14;

public class _01_ForLoop {
    public static void main(String[] args) {
        // 1 den 5 e kadar olan sayilari ekrana yazdiriniz.

        int i = 1;
        while(i<=5){

            System.out.println("w_i = " + i);
            i++;
            // 1 2 3 4 5
        }
        // for Dongusu
        for(int j = 1; ; j++){
            System.out.println("f_j: " + j);
        }
    }
}
