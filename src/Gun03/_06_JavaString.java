package Gun03;

public class _06_JavaString {
    public static void main(String[] args) {

        String ad = "Neeko"; // String : Kelime / Kellimeleri tutmak icin kullanilir, sinirlarini " " (cift tirnak) ile belirleriz
        String soyad = "Unver";

        System.out.println("ad = " + ad); // ad = Neeko
        System.out.println("soyad = " + soyad); // Soyad = Unver
        System.out.println("ad ve soyad = " + ad + " " + soyad); // ad ve soyad = Neeko Unver
    }
}
