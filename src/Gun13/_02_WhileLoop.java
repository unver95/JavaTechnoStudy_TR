package Gun13;

public class _02_WhileLoop {
    public static void main(String[] args) {
        // Ekrana 5 merhaba yazdiriniz
        int sayac = 0;

        // 0 1 2 3 4 5 -> 5 adim
        while (sayac < 5) { // iken yap
            // Sart saglandigi surece oarantezlerinin arasini calisitrir
            System.out.println("Hello World");
            sayac++; // sayac = sayac+1
        }
    }
}
